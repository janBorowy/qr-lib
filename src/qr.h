template <std::size_t N>
constexpr std::array<bool, N - 1> str_to_bits(const char (&str)[N]) {
    std::array<bool, N - 1> bits{};
    for (std::size_t i = 0; i < N - 1; ++i) {
        bits[i] = (str[i] == '1');
    }
    return bits;
}

